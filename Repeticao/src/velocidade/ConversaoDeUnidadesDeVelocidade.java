package velocidade;

public class ConversaoDeUnidadesDeVelocidade {
	public static float QuilometroHoraParaMetroSegundo(float quilometroHora) {
		return quilometroHora*0.2778f;
	}
	
	public static float QuilometroHoraParaMilhaHora(float quilometroHora) {
		return quilometroHora*0.6214f;
	}
	
	public static float QuilometroHoraParaPesSegundo(float quilometroHora) {
		return quilometroHora*0.9113f;
	}
}
